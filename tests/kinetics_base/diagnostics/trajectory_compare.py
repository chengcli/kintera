"""Multi-NT trajectory comparison kintera vs KINETICS-base.

Compares kintera no_grain output against KB oracle at the same NTIME
values: NT=1, 5, 10, 25, 50, 100. Reveals where in the integration
kintera diverges from KB, and which species recover with more steps.

Prerequisites:
- KB oracles at /tmp/kb_run_{NT}/fort.7 for each NT (generated by
  rebuilding KB for Linux and running with the corresponding NTIME).
- kintera dumps at /tmp/kt_traj_{NT}.npz, generated by running:
    for NT in 1 5 10 25 50 100; do
      KINTERA_TITAN_NTIME=$NT \
        KINTERA_TITAN_DUMP=/tmp/kt_traj_$NT.npz \
        python diagnostics/no_grain_stability.py
    done
"""

from __future__ import annotations

import pathlib
import numpy as np
import kintera as kt

NTS = (1, 5, 10, 25, 50, 100)
SPECIES_OF_INTEREST = (
    "H", "H2", "CH4", "CH3", "C2H6", "C2H2", "HCN", "N(2D)",
    "C2H3CN", "NH4+", "C6N2",
)
LEVELS = (10, 20, 30)


def load_kintera(nt: int) -> tuple[list[str], np.ndarray]:
    path = pathlib.Path(f"/tmp/kt_traj_{nt}.npz")
    d = np.load(path, allow_pickle=True)
    return [str(x) for x in d["species"]], np.asarray(d["concentration"])


def load_kb(nt: int) -> dict[str, list[float]]:
    return kt.parse_kinetics_base_atmosphere(f"/tmp/kb_run_{nt}/fort.7").species_profiles


def main() -> None:
    data = {nt: (load_kintera(nt), load_kb(nt)) for nt in NTS}
    print("kintera/KB ratio over NT (lev shown as L<n>)")
    print(f"{'species':<10s} {'lev':<3s} " + "  ".join(f"NT={n:<3d}" for n in NTS))
    print("-" * 80)
    for spp in SPECIES_OF_INTEREST:
        for L in LEVELS:
            row = f"{spp:<10s} L{L:<2d} "
            for nt in NTS:
                (sp_local, c_local), kb_p = data[nt]
                if spp not in sp_local or spp not in kb_p:
                    row += "    --  "
                    continue
                kv = float(c_local[L, sp_local.index(spp)])
                bv = float(kb_p[spp][L])
                if bv != 0 and kv != 0:
                    r = kv / bv
                    row += f" {r:>7.2f}" if 1e-3 < r < 1e3 else f" {r:>7.1e}"
                elif bv == 0 and kv == 0:
                    row += "    ----"
                elif bv == 0:
                    row += "     inf"
                else:
                    row += "    0.00"
            print(row)
        print()


if __name__ == "__main__":
    main()
